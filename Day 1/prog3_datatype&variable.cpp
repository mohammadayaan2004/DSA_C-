#include<iostream>
using namespace std;
int main()
{
    int a=10;                         // integer datatype
    float b=10.11;                    // floating point number datatype store upto 32 bits
    double c=10.1111;                 // floating point number datatype store upto 64 bits
    char d='A';                       // character datatype 
    string e="Hello World";           // string datatype
    bool f=true;                      // boolean datatype 
    
    cout<<"The  variable are :-"<<endl<<a<<endl<<b<<endl<<c<<endl<<d<<endl<<e<<endl<<f<<endl;

    return 0;
}